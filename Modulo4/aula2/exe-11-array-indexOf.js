// 'indexOf' Retorna o primeiro índice em que um elemento pode ser encontrado em um array
// 'lastIndexOf' Retorna o último índice em que um elemento pode ser encontrado em um array
const pessoas = ['Pedro', 'Lucas', 'André'];

const numeros = [10, 20, 30, 40, 50, 20, 30, 10];

console.log(pessoas.indexOf('Lucas'));
console.log(pessoas.lastIndexOf('André'));

console.log(numeros.indexOf(20));
console.log(numeros.lastIndexOf(20));


