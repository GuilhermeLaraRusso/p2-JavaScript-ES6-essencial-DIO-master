
const arr = [1, 2, 3, 4, 5];

// pega a posição inicial até antes da posição do segundo fator
console.log(arr.slice(0,2));

// pega a partir da segunda posição, incluindo a posição 2
console.log(arr.slice(2));

// pega a pultima posição
console.log(arr.slice(-1));

// pega a partir de 3 posições para a última até a última
console.log(arr.slice(-3));